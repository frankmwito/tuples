# unpack a tuple into several variables and print each variable
# Step 1: Define a tuple with several elements
my_tuple = ("Alice", 25, "Engineer", 5.8)

# Step 2: Unpack the tuple into variables
name, age, profession, height = my_tuple

# Step 3: Print each variable
print("Name:", name)
print("Age:", age)
print("Profession:", profession)
print("Height:", height)
